<h2 class="poma">Tesla Motors pretende ofrecer al cliente una experiencia convincente mientras que consigue ventas y beneficios de servicio que los fabricantes tradicionales no tienen en el modelo de distribución por franquicias y de talleres oficiales. Los clientes tratan directamente con personal de Tesla y no con franquiciados. Tesla quiere conseguir un mejor control de costes de inventario, gestión de garantía, servicio, fortalecimiento de la marca y obtención de comentarios de los clientes. Al ser propietario de la red comercial Tesla evita el conflicto de intereses en la estructura de los concesionarios tradicionales donde la venta de piezas en garantía y reparaciones del concesionario son un beneficio para el concesionario y un gasto para el fabricante.</h2>
