<h2  class="minaya">¿QUE ES UN AUTO MOVIL?</h2>
<h2 class="o">Un automóvil deportivo es un vehículo medio o pequeño, que puede ser para 2 o 4 pasajeros, diseñado para poder circular a altas velocidades. Al contrario que un automóvil de carreras, un automóvil deportivo está pensado para ser conducido en la vía pública. Tienen mejor aceleración, velocidad máxima, adherencia al asfalto, y mayor sistema de frenada que los automóviles normales, lo cual se logra mediante motores, frenos, suspensión, caja de cambios, neumáticos, chasis y carrocería especiales. Las carrocerías asociadas a un deportivo son cupé y descapotable </h2
